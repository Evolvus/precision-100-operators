CONTAINER=$1
FILE_NAME=$(echo $2 | cut -d ',' -f 2)
FILE_TYPE=$(echo $2 | cut -d ',' -f 3)
CONNECTION_NAME=$(echo $2 | cut -d ',' -f 4)

if test "$SIMULATION_MODE" = "TRUE"; then
   echo "        START SQL ADAPTOR $PRECISION100_EXECUTION_CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
   sleep $SIMULATION_SLEEP;
   echo "        END SQL ADAPTOR $PRECISION100_EXECUTION_CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
   exit;
fi

echo "        START SQL ADAPTOR $PRECISION100_EXECUTION_CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
source $PRECISION100_OPERATORS_FOLDER/sql-plus/conf/.sql-plus.env.sh

CONNECTION_STRING=$($PRECISION100_BIN_FOLDER/get-connection-string.sh "$CONNECTION_NAME")

$PRECISION100_BIN_FOLDER/audit.sh  $0 "PRE-SQl" "$CONTAINER / $FILE_NAME" "SQL" $0 "START"

sqlplus -s /nolog << EOL
CONNECT $CONNECTION_STRING
SET FEEDBACK OFF

@$PRECISION100_EXECUTION_CONTAINER_FOLDER/$CONTAINER/$FILE_NAME

EXIT

EOL
$PRECISION100_BIN_FOLDER/audit.sh  $0 "POST-SQl" "$CONTAINER / $FILE_NAME" "SQL" $0 "END"

echo "        END SQL ADAPTOR $PRECISION100_EXECUTION_CONTAINER_FOLDER/$CONTAINER/$FILE_NAME"
